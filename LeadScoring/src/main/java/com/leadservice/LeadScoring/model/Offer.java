package com.leadservice.LeadScoring.model;

public class Offer {
    private String name;
    private String valueProps;
    private String idealUseCases;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getValueProps() {
        return valueProps;
    }

    public void setValueProps(String valueProps) {
        this.valueProps = valueProps;
    }

    public String getIdealUseCases() {
        return idealUseCases;
    }

    public void setIdealUseCases(String idealUseCases) {
        this.idealUseCases = idealUseCases;
    }
}
